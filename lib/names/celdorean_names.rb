def celdorean_name(sex)
  table = Object.const_get("CELDOREAN_#{sex.upcase}")
  surname = roll_table(table)
  surname_ends_with_vowel = surname.match?(/[aeuio]\z/)
  suffix = case sex
           when "male"
             surname_ends_with_vowel ? "pur" : "apur"
           when "female"
             surname_ends_with_vowel ? "dar" : "adar"
           end
  "#{roll_table(table)} #{surname}#{suffix}"
end

